// Function to apply complete theme
function applyTheme(index) {
  const theme = themes[index];
  const root = document.documentElement;
  
  // Update all CSS variables
  root.style.setProperty('--primary-color', theme.primaryColor);
  root.style.setProperty('--primary-lite', theme.primaryLite);
  root.style.setProperty('--secondary-color', theme.secondaryColor);
  root.style.setProperty('--bg-gray', theme.bgGray);
  root.style.setProperty('--hover-gray-bg', theme.hoverGrayBg);
  root.style.setProperty('--border-gray', theme.borderGray);
  root.style.setProperty('--text-gray', theme.textGray);
  root.style.setProperty('--text-muted', theme.textMuted);
  root.style.setProperty('--bg-icon', theme.bgIcon);
  root.style.setProperty('--text-dark', theme.textDark);
  root.style.setProperty('--text-light', theme.textLight);
  root.style.setProperty('--bg-dark', theme.bgDark);
  root.style.setProperty('--bg-light', theme.bgLight);
  root.style.setProperty('--grey-color', theme.greyColor);
  root.style.setProperty('--black-color', theme.blackColor);
  root.style.setProperty('--box-shadow', theme.boxShadow);
  root.style.setProperty('--hover-shadow', theme.hoverShadow);
  root.style.setProperty('--hover-color', theme.hoverColor);
  root.style.setProperty('--title-weight', theme.titleWeight);
}

