        // Get job ID from URL parameter or sessionStorage
        function getJobId() {
            const urlParams = new URLSearchParams(window.location.search);
            const jobIdFromUrl = urlParams.get('job');
            
            if (jobIdFromUrl) {
                return jobIdFromUrl;
            }
            
            return sessionStorage.getItem('selectedJobId') || 'net-tech-lead';
        }

        // Populate job details on page load
        window.addEventListener('DOMContentLoaded', function() {
            const jobId = getJobId();
            const job = jobsData[jobId];

            if (!job) {
                console.error('Job not found');
                return;
            }

            // Update page title
            document.title = `${job.title} - Yottron`;

            // Update header information
            document.getElementById('jobTitle').textContent = job.title;
            document.getElementById('jobExperience').textContent = job.experience;
            document.getElementById('jobLocation').textContent = job.location;
            document.getElementById('jobSalary').textContent = job.salary;
            document.getElementById('jobRole').textContent = job.type;

            // Show/hide language field
            const languageItem = document.getElementById('languageItem');
            if (job.language) {
                languageItem.style.display = 'flex';
                document.getElementById('jobLanguage').textContent = job.language;
            } else {
                languageItem.style.display = 'none';
            }

            // Update job description
            document.getElementById('jobDescription').textContent = job.description;

            // Update responsibilities
            const responsibilitiesList = document.getElementById('jobResponsibilities');
            responsibilitiesList.innerHTML = '';
            job.responsibilities.forEach(item => {
                const li = document.createElement('li');
                li.textContent = item;
                responsibilitiesList.appendChild(li);
            });

            // Update qualifications
            const qualificationsList = document.getElementById('jobQualifications');
            qualificationsList.innerHTML = '';
            job.qualifications.forEach(item => {
                const li = document.createElement('li');
                li.textContent = item;
                qualificationsList.appendChild(li);
            });

            // Update preferred skills
            if (job.preferred && job.preferred.length > 0) {
                const preferredList = document.getElementById('jobPreferred');
                preferredList.innerHTML = '';
                job.preferred.forEach(item => {
                    const li = document.createElement('li');
                    li.textContent = item;
                    preferredList.appendChild(li);
                });
                document.getElementById('preferredSection').style.display = 'block';
            } else {
                document.getElementById('preferredSection').style.display = 'none';
            }

            // Update contact information
            document.getElementById('jobEmail').textContent = job.email;
            document.getElementById('jobEmail').href = `mailto:${job.email}`;
            
            const phoneFormatted = job.phone;
            const phoneWhatsApp = job.phone.replace(/\s+/g, '');
            document.getElementById('jobPhone').textContent = phoneFormatted;
            document.getElementById('jobPhone').href = `https://wa.me/${phoneWhatsApp}`;

            // Update closing date if available
            if (job.closing) {
                document.getElementById('jobClosing').textContent = job.closing;
                document.getElementById('closingDateItem').style.display = 'flex';
            } else {
                document.getElementById('closingDateItem').style.display = 'none';
            }

            // Update apply buttons
            document.getElementById('emailBtn').href = `mailto:${job.email}?subject=Application for ${job.title}`;
            document.getElementById('whatsappBtn').href = `https://wa.me/${phoneWhatsApp}?text=Hi, I'm interested in applying for the ${job.title} position`;
        });